function changeimage()
{
    let srkimage=document.getElementById("srk")
    srkimage.style.border="10px solid green"
    srkimage.setAttribute("src","DOMIMAGES/srk2.jpg")
}
function removechange()
{
    let srkimage=document.getElementById("srk")
    srkimage.setAttribute("src","DOMIMAGES/srk1.jpg")
}
